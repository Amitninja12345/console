package org.jboss.hal.client.configuration;

import com.google.gwt.dom.client.Document;
import com.google.gwt.dom.client.PreElement;
import com.google.gwt.event.dom.client.ClickEvent;
import com.google.gwt.user.client.ui.Button;
import com.google.gwt.user.client.ui.Composite;
import org.jboss.errai.ui.nav.client.local.Page;
import org.jboss.errai.ui.shared.api.annotations.DataField;
import org.jboss.errai.ui.shared.api.annotations.EventHandler;
import org.jboss.errai.ui.shared.api.annotations.Templated;
import org.jboss.hal.dmr.dispatch.Dispatcher;
import org.jboss.hal.dmr.model.Operation;
import org.jboss.hal.dmr.model.ResourceAddress;

import javax.inject.Inject;

import static org.jboss.hal.dmr.ModelDescriptionConstants.INCLUDE_RUNTIME;
import static org.jboss.hal.dmr.ModelDescriptionConstants.READ_RESOURCE_OPERATION;

/**
 * @author Harald Pehl
 */
@Templated("#content")
@Page(path = "configuration")
public class Configuration extends Composite {

    @Inject Dispatcher dispatcher;

    @DataField @Inject Button readRootResource;
    @DataField PreElement result = Document.get().createPreElement();

    @EventHandler("readRootResource")
    public void onReadRootResource(ClickEvent event) {
        Operation op = new Operation.Builder(READ_RESOURCE_OPERATION, ResourceAddress.ROOT)
                .param(INCLUDE_RUNTIME, true).build();
        dispatcher.execute(op, payload -> result.setInnerText(payload.toString()));
    }
}
